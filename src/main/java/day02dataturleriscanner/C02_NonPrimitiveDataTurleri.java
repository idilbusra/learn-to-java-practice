package day02dataturleriscanner;

public class C02_NonPrimitiveDataTurleri {
    public static void main(String[] args) {
        int sayi=10;
        String str= "Java candir";

        System.out.println(sayi);//10
        System.out.println(str.toLowerCase());//java candir
        System.out.println(str.toUpperCase());//JAVA CANDIR

        /*
        1-primitive data turleri sadece deger saklamanin yaninda hazir methodlar da barindirir
        Bu hazir methodlar sayesinde biz tanimlanmis islemleri kolayca yapabiliriz

        2-primitive data turlerinin isimleri kucuk harf ile baslar
        non-primitive data turlerinin isimleri buyuk harf ile baslar




         */

    }
}
